package com.haripriya.practice;
//Class Declaration
public class AddTwoNumbers        
{
   public static void main(String args[]) //Main Function
   {
      // Variable Declaration
      int x, y, z;
      //Assigning 10 to the variable x.      
      x = 10;
      //Assigning 20 to the variable y.           
      y = 20;
      
      /*Expression to Add two variables x, y and save the result to z */           
      z = x + y; 
      
      /**This line output the value of z on the Screen**/    
      System.out.println("Sum of x and y = "+z); 
   }
}
